<h1>Jak szybko stworzyć atrakcyjną stronę?</h1>

<h2>Wstęp</h2>

<p>Nie każdy początkujący użytkownik Pająka od razu zauważa cały potencjał tkwiący w programie. W pierwszej chwili przerażać może ogrom różnych narzędzi, ikon, itp. W rozpoczętym właśnie poradniku postaramy się prezentować co ciekawsze możliwości programu nie od razu zauważane. W tym artykule przedstawiamy metodę na szybkie przygotowanie atrakcyjnego wizualnie serwisu internetowego na bazie szablonów wbudowanych w Pająka.</p>

<h2>O szablonach czyli Makrokodach </h2>

<p>Najszybszą metodą stworzenia nowego i atrakcyjnego serwisu internetowego jest wykorzystanie szablonów wbudowanych w program. Szablony gotowych stron zostały przygotowane przez profesjonalnego grafika oraz wbudowane w program z wykorzystaniem mechanizmu tzw. <a href="macrocodes.php">Makrokodów</a>. </p>

<h3>Kreator nowej strony z szablonu</h3>

<p>W celu ułatwienia tworzenia nowych dokumentów przez początkujących webmasterów, Pająk został wyposażony w narzędzie <a href="tool_new_page_wizard.php">Kreator nowej strony z szablonu</a>. Narzędzie to w prosty i intuicyjny sposób umożliwia przygotowanie i zapisanie na dysku kompletnego dokumentu wraz z grafikami i innymi niezbędnymi plikami. Stanowi ono alternatywny sposób wykorzystania opisanych niżej szablonów stron dostępnych w opisanej dalej zakładce Makrokody.</p>

<h3></h3>

<p>Makrokody to w uproszczeniu modyfikowalne szablony różnego rodzaju przykładów, które opatrzone w zmienne mogą być modyfikowane przed wstawieniem ich w treść strony WWW. W Pająku wśród Makrokodów znaleźć można takie elementy jak gotowe skrypty JavaScript, bannery, często używany kod PHP, itp. czy też kompletne szablony stron WWW czy też stron internetowych.</p>

<p>Aby zapoznać się z zawartością Makrokodów należy z panelu w lewej części okna programu wybrać <a href="main_tabs_macrocodes.php">zakładkę Makrokody</a>. Na tej zakładce wyświetlane jest drzewko z różnymi kategoriami makrokodów. W przypadku gdy rozwinie się wybraną gałąź i wybierze jakiś makrokod z listy, ujawniają się dodatkowe opcje takie jak podgląd makrokodu lub jego źródła, a także dodatkowe zakładki ze <a href="macrocodes_editor_vars.php">zmiennymi</a> i <a href="macrocodes_editor_blocks.php">blokami</a> jeśli makrokod posiada zdefiniowane takowe zmienne. Zmienne pozwalają na modyfikację wyglądu bądź treści makrokodów w zależności od preferencji jeszcze przed ich wstawieniem na stronę. </p>

<h2>Wybieranie szablonu dla strony WWW</h2>

<p>Makrokody zawierają również, jak już wspomniano, gotowe szablony stron internetowych. Aby móc przeglądać dostępne szablony należy rozwinąć gałąź "Szablony stron".</p>

<p>Po rozwinięciu wspomnianych wyżej gałęzi ukaże się lista nazw szablonów. Szablony można przeglądać ponownie rozwijając gałąź. W związku z tym, że oglądamy szablony stron po rozwinięciu gałęzi na liście znajdą się nazwy stron wchodzących w skład serwisu, czyli np. "O mnie", "Kontakt", "Strona główna", itp. Jeśli wybierzesz jedną ze stron na dole zakładki Makrokody pojawi się podgląd makrokodu. Okienko podglądu jest niewielkie, ale jest możliwość powiększenia podglądu na pełny ekran za pomocą przycisku lupy znajdującego się nad mini-podglądem makrokodu. Można również wyświetlić podgląd kodu źródłowego makrokodu zaznaczając odpowiednią opcję powyżej mini-podglądu.</p>

<h2>Modyfikowanie wybranego szablonu </h2>

<p>Kiedy już wybrany zostanie serwis, możemy przystąpić do tworzenia poszczególnych składników serwisu. Zacznijmy od strony głównej wybierając pozycję na liście o nazwie "Strona główna". Jak już wspomniano, w dolnej części zakładki Makrokody wyświetli się podgląd makrokodu oraz dodatkowe zakładki o nazwach "Zmienne" i "Bloki", za pomocą których możemy sterować zawartością strony. Na rysunku obok przedstawiony jest wybór szablonu. Aby ułatwić sobie definiowanie wyglądu strony, dobrze jest włączyć podgląd szablonu na cały ekran za pomocą przycisku lupy znajdującego się nad mini-podglądem makrokodu. Dzięki temu cały szablon będzie widoczny, a wprowadzane zmiany będzie można łatwo zaobserwować w działaniu.</p>

<p>Aby dokonać zmian w szablonie przełączamy się teraz na zakładkę "Bloki", co widać na rysunku obok. Zakładka "Bloki" to lista części składowych strony lub serwisu. W tym wypadku, dla wybranego przez nas szablonu o nazwie "Speedbike" jest to definicja menu głównego strony, czyli odsyłaczy do innych stron wchodzących w skład serwisu. Jeśli z jakiś powodów nie chcesz, aby na Twojej stronie znajdowała się galeria czy też dział "Linki", wyłącz tę opcję na zakładce "Bloki". Na podglądzie makrokodu od razu zauważysz wprowadzoną zmianę. Menu główne ulegnie modyfikacji i nie będzie widoczna ta pozycja menu, którą wyłączyłeś.</p>

<p>Kolejnym miejscem, które teraz odwiedzimy jest zakładka "Zmienne". Pełni ona podobną funkcję do zakładki "Bloki" jednakże jest tam dostęp do dużo większej liczby różnego rodzaju opcji pozwalających na wpływanie na zawartość strony. Wygląd zakładki "Zmienne" przedstawiony został na rysunku poniżej.</p>

<p>Pokażemy teraz jak zmienić kilka z opcji, które mają wpływ na wygląd strony. Zaczniemy od treści strony wyświetlanej w szablonie. W tym celu należy przełączyć się na wspomnianą zakładkę "Zmienne" i z długiej listy opcji wskazać tę, która jest opisana jako "Treść dokumentu". Prawa część klikniętego wiersza stanie się możliwa do edycji i teraz można już wpisać w tym miejscu treść, którą chciałbyś umieścić na swojej stronie głównej. Może tam być powitanie, zaproszenie do przeglądania czy jakieś inne ogólne informacje - jak to na stronie głównej. Na rysunku obok możesz porównać jak to wygląda w praktyce. W naszym wypadku zastąpiliśmy przykładowy tekst fragmentem:</p>
<code>
&lt;BR&gt;&lt;BR&gt;&lt;BR&gt;&lt;BR&gt;&lt;BR&gt;&lt;BR&gt;<br>
Witaj na mojej stronie. Jest mi bardzo miło, że tutaj zawitałeś. Mam nadzieję, że spędzisz tutaj miłe chwile i przeczytasz ciekawe informacje. Kliknij w wybrane menu, aby zajrzeć do poszczególnych części mojego serwisu internetowego. &lt;br&gt;&lt;br&gt; Jeśli chcesz do mnie napisać e-mail oto mój adres: &lt;a href="mailto:mojadres@mojserwer.pl"&gt;mojadres@mojserwer.pl&lt;/a&gt;.</code>

<p>Jak widać w tym polu można używać HTML, aby umieścić odsyłacze czy też zdefiniować jakieś formatowanie. </p>

<p>Pozostałe zmienne w makrokodzie pozwalają na definiowanie wielu różnych aspektów edytowanego szablonu, są to odsyłacze do pozostałych plików serwisu, kolory tekstu, odsyłaczy, sposób wyrównania strony, itp. </p>

<p>Za pewne zauważyłeś, że w każdym makrokodzie znajduje się logo o wyglądzie "Imię Nazwisko - Moja strona domowa". Jak zmienić to logo pokażemy w dalszej części artykułu.</p>

<h2>Zapisywanie gotowej strony na dysku</h2>

<p>Aby zapisać zmodyfikowany szablon na dysku jako gotową stronę WWW należy wybrać polecenie "Utwórz nowy dokument z makrokodu". Polecenie to znajduje się w menu pod piątym od lewej przyciskiem w górnej części makrokodu. Przycisk nazywa się "Wstawianie makrokodu". Jeśli klikniesz wspomniane polecenie program poprosi o wskazanie miejsca na dysku, gdzie ma być zapisana strona. Dobrze jest przeznaczyć na to osobny folder. Zalecamy również zmianę domyślnej nazwy pliku strony głównej np. na index.html, bowiem serwery WWW zazwyczaj nie obsługują nazw plików z polskimi literami. </p>

<p>Po wybraniu nazwy pliku i zatwierdzeniu zapisu dokument zostanie utworzony i otwarty w Pająku, gdzie możesz poddać go dalszej edycji. W międzyczasie możesz zobaczyć komunikat z pytaniem o dodanie deklaracji kodowania znaków narodowych. Zalecamy kliknąć przycisk TAK, dzięki czemu polskie znaki będą prawidłowo wyświetlane w większości przeglądarek internetowych.</p>

<p>Jak już wspomniano, utworzoną i otwartą automatycznie do edycji stronę możesz edytować w Pająku zmieniając różne parametry HTML. Dodać tutaj należy, że strona jest zapisywana na dysku wraz z wszystkimi plikami graficznymi wchodzącymi w skład szablonu. Pliki te są zapisywane w folderze o nazwie hid_mc_gfx.</p>

<h2>Zmiana logo serwisu na własne</h2>

<p>Wspomnieliśmy już, że logo serwisu jest w każdym wypadku identyczne o postaci "Imię Nazwisko - Moja strona domowa". Aby je zmienić będziesz potrzebował programu graficznego, w którym należy otworzyć plik logo.gif znajdujący się w folderze hid_mc_gfx. W programie graficznym możesz zmienić plik logo.gif tak, aby zamiast domyślnego tekstu znajdowało się tam Twoje nazwisko lub ksywka lub cokolwiek innego, co będzie pasować do Twojej koncepcji strony. Pamiętać jednak należy, żeby zachować wymiary obrazka logo ze względu na układ strony. Dzięki temu strona zachowa swój pierwotny układ.</p>

<h2>Tworzenie kolejnych składników serwisu</h2>

<p>Aby utworzyć kolejne strony serwisu, np. "O mnie", "Linki", "Kontakt" itp. należy wybrać następne, odpowiednio nazwane szablony na zakładce Makrokody i powtórzyć kroki opisane powyżej dla strony głównej zmieniając przy tym treść, czy też kolorystykę według własnego uznania. Zapisując kolejne strony na dysku należy pamiętać, aby umieszczać je w tym samym folderze co strona główna serwisu, bowiem wtedy będziesz miał dobrze zorganizowany serwis, a wszystkie powiązania będą działać poprawnie. Uwaga! Jeśli zmieniasz nazwy plików pod jakimi zapisujesz szablony, pamiętaj aby te nazwy wskazać na zakładce "Zmienne". W innym wypadku powiązania między stronami serwisu mogą nie działać. Najlepiej przed przystąpieniem do tworzenia serwisu zapisać sobie gdzieś na kartce nazwy dla poszczególnych stron i przypisywać je do odpowiednich zmiennych już od momentu tworzenia strony głównej.</p>

<h2>Publikacja serwisu w sieci Internet</h2>

<p>Przed publikacją serwisu w sieci należy najpierw przetestować jego działanie na komputerze lokalnym. W tym celu można otworzyć jedną z wygenerowanych stron w przeglądarce i "pochodzić" po jej odsyłaczach sprawdzając czy nie ma błędnych odsyłaczy i wszystko działa jak należy. Można również w tym celu wykorzystać narzędzia weryfikacji wbudowane w Pająka, jednak to zadanie jest przeznaczone dla bardziej zaawansowanych użytkowników, co nie oznacza jednak, że nie warto się z nim zapoznać.</p>

<p>Aby opublikować stronę w Internecie należy przede wszystkim mieć miejsce na publikację, czyli innymi słowy konto internetowe z możliwością publikowania stron WWW. Posiadając takie konto należy skonfigurować je w Pająku w <a href="settings_network_ftpservers.php">ustawieniach FTP</a> ("Dostosuj sieć i połączenia"). </p>

<p>Po zdefiniowaniu konta możesz się z nim połączyć za pomocą panelu zasobów po prawej stronie ekranu i <a href="main_tabs_myftpservers.php">zakładki "Moje serwery"</a>, która się tam znajduje. Również w tym wypadku dodatkowych informacji szukaj w pomocy do programu. Pamiętaj, aby przesłać na serwer wszystkie pliki w takim samym układzie jak znajdują się na dysku, tj. wg odpowiednich folderów, z podfolderami i przypisanymi grafikami. W innym wypadku strona może nie działać poprawnie. Również i w tym wypadku zalecamy przetestowanie opublikowanego serwisu już z sieci Internet.</p>

