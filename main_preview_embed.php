<h1>Zakładka "Przeglądanie"</h1>

<!-- treść pomocy -->




<p>Zakładka "Przeglądanie" umieszczona jest w <a href="main_tabs_general.php#tabs_center">środkowym panelu</a>, którego nie możesz wyłączyć. Na tej zakładce możesz przeglądać aktualnie tworzony dokument. Umożliwia ona tez podgląd szablonów, jeśli na <a href="main_tabs_macrocodes.php">zakładce Szablony</a> została ustawiona taka opcja.</p>

<p>Ze względu na bardzo szybko zmieniająca się ofertę programową wielkich firm i coraz nowsze rozszerzenia wprowadzane przez nie do języka HTML, zdecydowano, że Pająk nie zostanie wyposażony we własny system podglądu tworzonej strony. Położono natomiast silny nacisk na ułatwienie pracy z dostępnymi na rynku przeglądarkami.
</p>


<p>Z poziomu Pająka można wykonać natychmiastowy podgląd pola edycji (domyślny klawisz <tt>F11</tt>) aktualnie edytowanej strony za pomocą zakładki "Przeglądanie". Funkcja ta wymaga zainstalowanej w systemie jednej z przeglądarek

<ul>
	<li>MS Internet Explorer w wersji 3.02 lub nowszej</li>
	<li>Mozilla 1.x</li>
</ul></p>

<p><b>Uwaga! Podgląd w Mozilli 1.6</b><br />
W chwili obecnej, w celu uzyskania działającego wbudowanego podglądu w Mozilli 1.6 może być konieczne zainstalowanie odpowiedniej kontrolki dostępnej pod adresem: <a href="https://www.adamlock.com/mozilla/MozillaControl1712.exe" target="_blank">https://www.adamlock.com/mozilla/MozillaControl1712.exe</a>.


<h2>Menu zakładki "Przeglądanie"</h2>

Zakładka posiada menu, które pozwala dostosować opcje przeglądania aktualnie edytowanych dokumentów:

<p><b>Pole adresu</b><br />
W tym polu pojawia się adres aktualnie przeglądanego dokumentu. Możesz też tu wpisać adres innego dokumentu i nacisnąć <tt>Enter</tt>, by go wyświetlić.

<p><b>Przejście do poprzednio przeglądanej</b><br />
Kliknij, aby wrócić do poprzedniej strony.

<p><b>Przejście do kolejnej, już przeglądanej</b><br />
Kliknij, aby przejść do następnej strony, juz przeglądanej (jeśli z niej wróciłeś)

<p><b>Odświeżenie podglądu</b><br />
kliknij, aby odświeżyć podgląd strony w oknie.

<p><b>Wydrukuj aktualna stronę</b><br />
Kliknij, aby wydrukować przeglądaną stronę.

<p><b>Edytuj stronę w Pająku</b><br />
Kliknij, aby otworzyć przeglądaną stronę w edytorze.

<p><a name="ruler"></a><b>Pokaż, lub ukryj linijkę</b><br />
Kliknij, aby wyświetlić, bądź ukryć linijkę. Możesz wybrać jednostki, jakie maja być pokazywane na linijce (piksele, milimetry, centymetry, cale).

<p><b>Wykorzystywana przeglądarka</b><br />
Kliknij, aby wybrać przeglądarkę używana do przeglądania w tej zakładce. O ile jest zainstalowana w systemie, możesz skorzystać z przeglądarki:
<ul>
	<li>MS Internet Explorer</li>
	<li>Mozilla</li>
</ul></p>

<p><a name="width"></a><b>Szerokość obszaru podglądu</b><br />
Kliknij, aby ustawić szerokość obszaru podglądu w pikselach. Dostępne są standardowe szerokości ekranu w Windows (640, 800, 1024, 1600). Własność ta pozwala obejrzeć stronę w docelowej szerokości, w jakiej będzie ona oglądana przez gości projektu.


<p><b>Mapowanie folderu na adres</b><br />
Kliknij, aby wybrać, lub wyłączyć <a href="service_properites.php#service_properties_mappings">mapowanie dla przeglądania</a>. Mapowanie to umożliwia przeglądanie stron za pomocą adresu sieciowego, a nie ścieżki na dysku. Jest ustawiane osobno dla każdego projektu i po zmianie projektu na zakładce Moje projekty zmienia się automatycznie na domyślne mapowanie tego projektu. W celu odnalezienia mapowania na liście, nazwa bieżącego projektu wyróżniona jest pogrubieniem.


