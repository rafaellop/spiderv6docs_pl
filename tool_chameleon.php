<h1>Narzędzia - Kameleon - Zmiana nazw wielu plików</h1>



<p>Kameleon świadczy nieocenioną pomoc webmasterom, którzy muszą zarządzać rozbudowanymi witrynami składającymi się z wielu plików składowych. Za pomocą tego narzędzia możesz hurtowo zmienić nazwy wybranych plików znajdujących się we wskazanych folderach. Program umożliwia tworzenie własnych reguł zmiany nazw, za pomocą których możesz na przykład kolejno ponumerować wszystkie pliki graficzne.

<p>Kameleon posiada przyjazny interfejs, który prowadzi za rękę przez wszystkie etapy procesu zmiany nazw, począwszy od wyboru folderów, przez przygotowanie listy plików do konwersji nazw, aż po ustalenie reguł konwersji i finalny proces, który przebiega szybko i sprawnie.

<h2>Uruchamianie</h2>

<p>Kameleona możesz uruchomić za pomoca polecenia oznaczonego ikoną pokazaną powyżej znajdującego się w <a href="main_menu_main.php">Menu głównym</a> / <a href="main_menu_file.php">Plik</a>, a także za pomocą menu podręcznego listy plików na zakładce <a href="main_tabs_myservices.php">Moje projekty</a>. Alternatywnym sposobem uruchamiania narzędzia jest zaznaczenie kilku plików na liście i wciśnięcie klawisza F2.

<p>Pierwsza plansza tego narzędzia pełni funkcję informacyjną i jest powitaniem w narzędziu.

<p>Pomiędzy kolejnymi planszami poruszasz się za pomocą przycisków Dalej i Wstecz.

<h2>Wybór folderów</h2>

<p>Druga plansza pozwala na wybranie folderów, w których znajdują się pliki, którym chcesz przypisać nowe nazwy. Kolejne foldery dodasz za pomocą przycisków obok listy. Poniżej listy znajduje się przełącznik <b>Uwzględnij podfoldery</b>, który powoduje, że przy tworzeniu listy plików, których nazwy będą zmieniane, uwzględniona zostanie również zawartość podfolderów wskazanych na liście folderów.

<h2>Wybór plików</h2>

<p>Trzecia plansza tworzona jest automatycznie na podstawie zawartości folderów wybranych na poprzedniej planszy. Dodatkowo możesz tutaj dodać pliki, które nie wchodzą w skład folderów wybranych wcześniej. Możesz też usunąć wybrane pliki z listy. Lista prezentuje nazwę pliku, typ, czyli rozszerzenie, datę ostatniej modyfikacji oraz pełną ścieżkę.

<h2>Określenie reguł zamiany</h2>

<p>Kolejna plansza pozwala na ustalenie reguł zmiany nazw plików. Reguły tworzy się w osobnym okienku opisanym w temacie <a href="tool_chameleon_rules.php">Edytor reguł konwersji</a>. Kolejne reguły przypisywane są do listy. Możesz je oczywiście modyfikować. Możesz też tworzyć różne reguły dla różnych typów plików.

<h2>Plan konwersji</h2>

<p>Po wybraniu reguł konwersji i przejściu na kolejną planszę pokazany zostanie plan konwersji, a więc raport pokazujący jak będą nazwane pliki po dokonaniu zmiany nazw. Lista znajdująca się na tej planszy wyświetla w kolejnych kolumnach pełną oryginalną ścieżkę i ścieżkę po konwersji, nazwę oryginalną i nazwę po konwersji. Jeśli plik jest oznaczony do usunięcia, będzie to zaznaczone w kolumnie Usuń. Na liście nie znajda się pliki, których nazwy, mimo wprowadzenie reguły, nie zostaną zmienione. 
<p>Raport można zapisać na dysk w postaci HTML jak i TXT, a niektóre zaplanowane zadania można usuwać za pomocą przycisku oznaczonego minusikiem. </p>

<p>Po upewnieniu się co do zmian, które zostaną przeprowadzone należy kliknąć przycisk "Zakończ", a program przeprowadzi konwersję.

