<h1>HTML Help - Kompilacja dokumentu HTML Help</h1>


<p>Za pomocą tego niewielkiego okienka dialogowego uruchamiamy kompilację przygotowanego projektu. Przed rozpoczęciem kompilacji projektu  należy pamiętać, aby w <a href="settings_addtools_htmlhelp.html">ustawieniach</a> zdefiniowana była ścieżka dostępu do kompilatora HTML Help wersja 1. Kompilator zawarty jest w pakiecie MS HTML Help Workshop, który możesz pobrać za darmo ze strony <a href="https://msdn.microsoft.com/en-us/library/windows/desktop/ms669985(v=vs.85).aspx" target="_blank">Microsoft MSDN</a>.

<h2>Uruchamianie</h2>

<p>Opisywane narzędzie możesz uruchomić za pomocą przycisku oznaczonego ikoną pokazaną powyżej. Przycisk uruchomienia tego okna znajduje się np. w menu HTML Help oraz na pasku narzędzi HTML Help.

<h2>Kompilacja projektu HTML Help</h2>

<p><b>Aktualnie otwarty dokument projektu</b><br />
Opcja ta uaktywniona zostaje, gdy otwarty do edycji w oknie Pająka i aktywny jest dokument projektu HTML Help
</p>
<p><b>Wskazany plik projektu (*.hhp)</b><br />
W polu tym możemy wpisać nazwę pliku projektu HHP wraz z pełną ścieżką dostępu. Można też skorzystać z przycisku obok pola, aby wyszukać ten plik na dysku.
</p>


