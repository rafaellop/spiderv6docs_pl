<h1>Edytor schematów kolorowania - Słowa kluczowe</h1>

<!-- treść pomocy -->

<p>Słowa kluczowe, to ciągi znaków, które są na stałe wpisane do danego schematu kolorowania. Są to najczęściej nazwy funkcji i instrukcji w językach programowania, nazwy znaczników i ich atrybutów w języku HTML itp. </p>
<p>Poszczególne rodzaje słów kluczowych pogrupowane są w zbiory. Możliwe jest utworzenie nieograniczonej ilości zbiorów słów kluczowych, z których każdy może mieć przypisane inny styl kolorowania.</p>
<p><b>Uwaga! </b>Nie należy przesadzać z ilością zbiorów i ilością słów w zbiorach przy korzystaniu ze starszego sprzętu. Obciąża to mocno program i może zauważalnie spowolnić pracę edytora, szczególnie przy przełączaniu się między dokumentami. Dlatego też Pająk posiada wiele różnych schematów kolorowania składni i umożliwia ich przełączanie w trakcie pracy, by  jak najmniej obciążać system. </p>

<p><b>Włącz kolorowanie słów kluczowych</b><br />
Jeśli zaznaczysz tę opcję, możliwe będzie zdefiniowanie zbiorów słów kluczowych. Dla każdego zbioru słów kluczowych można utworzyć i przypisać styl kolorowania. 
</p>

<p><b>Zbiory</b><br />
Lista zawiera zbiory słów kluczowych zdefiniowanych w schemacie. Zbiory można dodawać, usuwać i edytować po kliknięciu w ikonę z prawej strony listy zbiorów.</p>
<p><b>UWAGA!</b> Zbiory słów kluczowych można układać w dowolnej kolejności za pomocą przeciągania. Jeśli w dwóch zbiorach wystąpi to samo słowo kluczowe, będzie ono kolorowane wg stylu dla zbioru, który wystąpi wcześniej (wyżej na liście).</p>

<ul>
<li><b>Edytuj zbiory słów kluczowych</b><br />
Kliknij, aby zmienić nazwę zbioru, a także przypisać do zbioru, bądź utworzyć nowy styl kolorowania.</li>
</ul>

<p><b>Bieżący zbiór słów kluczowych</b><br />
Zawiera wszystkie słowa przypisane do aktualnie wybranego zbioru. Słowa kluczowe wybranego zbioru możesz dodawać, odejmować i edytować po kliknięciu w ikonę z prawej strony listy słów kluczowych. Słowa na liście automatycznie sortują są wg kolejności alfabetycznej.</p>

<ul>
<li><p><b>Wczytaj słowa kluczowe</b><br />
Kliknij, aby wczytać zbiór słów kluczowych z pliku tekstowego. Każde słowo kluczowe w pliku musi być umieszczone w osobnej linii.</p></li>

<li><p><b>Zapisz słowa kluczowe</b><br />
Kliknij, aby zapisać zbiór słów kluczowych do pliku tekstowego.</p></li>
</ul>

