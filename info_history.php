<h1>Historia programu</h1>

<p>Pająk w najnowszej wersji jest oznaczony numerem V6. Przez kilka dobrych lat program funkcjonował z powodzeniem jako wersja 5, która już w momencie powstania wspierała najnowocześniejsze technologie Web, jak HTML5 i CSS3. Przez te kilka lat od premiery jego ostatniej wersji, miała miejsce prawdziwa rewolucja w technologiach web, a zwłaszcza nastąpił rozwój urządzeń mobilnych, które obecnie napędzają świat. Niestety Pajączek, w tym zmieniającym się otoczeniu, obrósł trochę mchem, a i prace nad wersją 6 przeciągały się niemiłosiernie, gdy w obliczu zmian w życiu osobistym oraz eksplozji technologicznej, przyjęte założenia okazały się zbyt ambitne, jak na jedną osobę, która tworzy program. Mnie. Przepraszam.</p>

<p>Postanowiłem, że nie chcę jednak dzieła swojego życia skazywać na zapomnienie i odświeżyć je nieco, dodając wsparcie dla nowych technologii i dostosowując je choć trochę do zmian, które nastąpiły. Wskutek tego V6 to raczej ewolucja niż rewolucja. Jest to obecnie po prostu dobre narzędzie do codziennej pracy z kodem. Ja osobiście wykorzystuję je od lat nie tylko jako edytor stron, ale udało mi się w nim również stworzyć kilka aplikacji dla Android i iOS, które zdobyły względną popularność i pomagają ludziom w życiu, czy pracy. Te funkcje wbudowałem również w V6, aby i inni mogli skorzystać na moim doświadczeniu.</p>

<p>Wiele osób wychowanych na Pajączku 5 i wcześniejszych wersjach, odeszło do innych zajęć, albo przesiadło się na inne narzędzia, narzucane przez pracodawców lub zmieniający się rynek. Mam jednak nadzieję, że bieżące wydanie zainteresuje Was nie tylko jako ciekawostka z przeszłości, ale również coś przydatnego w codziennej pracy kodera front- czy backendu. Marzy mi się również, że młodzi webmasterzy i koderzy oraz aspirujące do tego grona osoby, zainteresują się nim przynajmniej na tyle, aby z jego pomocą wkroczyć w świat kodowania.</p>

<p>Pająk ewoluował od prostego generatora stron pracującego w trybie znakowym DOS, aż do obecnej postaci programu wspomagającego najnowsze technologie stosowane w Sieci.</p>
<p>Poniżej możesz zapoznać się ze skróconą historią wersji programu w kolejności od najnowszej do najstarszej wersji.</p>

