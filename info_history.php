<h2>Pająk 2000 4.x</h2>

<p>Pająk 2000 rozwijany był od dnia 27 października 1998, kiedy to ukazała się wersja 4.0, przez okres ok. 2,5 roku. W okresie tym ukazało się w sumie 12 aktualizacji. Ostatnia z nich - Pająk 2000 4.8.1. została udostępniona w dniu 12 kwietnia 2001.</p>

<p>W trakcie ewolucji programu dodawane były liczne funkcje,wprowadzono dodatkowe udogodnienia, dodano liczne schematy kolorowania składni itp.</p>

<!-- nagłowek podtematu -->
<h2>Pająk 3.0 Light</h2>

<p>19 października 1998<br />
Jest to odnowiona wersja generatora strona WWW (wersji 1.11 i 1.12). Tym razem znacznie rozbudowana m.in. o opcje publikowania na serwerze za pomocą protokołu FTP.</p>

<!-- nagłowek podtematu -->
<h2>Pająk 2.x</h2>

<p>Pająk 2 o mały włos byłby jedynie kontynuacją wersji 1.x. Jednakże w dniu  19 października 1998 roku miała miejsce awaria systemu, wskutek czego utracone zostały kody źródłowe prawie ukończonej wersji programu. Zdarzenie to zmobilizowało mnie do przedefiniowania założeń programu i ponownego rozpoczęcia pracy nad wersją 2.0, ale tym razem już całkowicie inną.</p>

<p>Premiera Pająka 2.0 dla Windows 3.x/95/NT miała miejsce w dniu 9 maja 1997 roku. W dniu 10 sierpnia 1997 roku został on zaktualizowany do wersji 2.1.2. </p>

<!-- nagłowek podtematu -->
<h2>Pająk 1.x</h2>

<p><b>10 listopada 1996 - Pająk 1.11 i Pająk 1.12</b><br />
Były to w zasadzie te same programy, jednakże pracujące odpowiednio pod kontrolą Windows 3.x i  Windows 95/NT. Te wersje są nadal dostępne do ściągnięcia z naszego internetowego archiwum.</p>

<p><b>28 sierpnia 1996 - Pająk 1.0 dla Windows 3.x</b><br />
Pierwsza wersja Pająka dla systemu Windows. Na tym etapie Pająk był prostym generatorem stron WWW działającym na podobnej zasadzie jak wersja dla DOS, ale wyposażonym w graficzny interfejs użytkownika obsługiwany myszką.</p>

<p><b>4 kwietnia 1996 - Pająk dla DOS i Linux'a</b><br />
Prosty program generujący strony WWW na podstawie odpowiedzi na zadawane pytania. Ta wersja pracowała pod kontrolą systemu DOS lub Linux w trybie znakowym. Powstała w języku C jako praca semestralna na kierunku Informatyki i Ekonometrii Akademii Ekonomicznej w Krakowie (obecnie Uniwersytet Ekonomiczny).

