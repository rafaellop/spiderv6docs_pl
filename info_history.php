<p>Premiera programu ma miejsce w 2021 roku w 25 lat po premierze pierwszej wersji dla DOS. Program ma już ćwierć wieku. To jest nie do uwierzenia... <a href="info_whatsnew.php">Co nowego?</a></p>

<h2>Pajączek 5.x</h2>

<p>W 2003 roku, po półtorarocznej przerwie od ostatniej wersji Pająka 2000 (podwersja 4.8.1) ukazuje się, z niecierpliwością oczekiwany przez użytkowników Pająk 5.0, którego ostatnia wersja 5.9.9 ukazuje się w kwietniu 2013 roku. W wersji piątej nastąpił podział na dwie linie programu:</p>
<ul>
        <li><b>Professional</b> - przeznaczony dla profesjonalistów program w pełnej wersji. Wspomaga zaawansowane technologie, jest bogato wyposażony w narzędzia wspomagające pracę w różnych językach programowania, prace grupowa itp.</li>
        <li><b>Standard</b> - nie posiada wielu zaawansowanych narzędzi. Jednak w pełni wystarcza do przygotowania nawet dość zaawansowanych serwisów wykonanych w oparciu o DHTML. </li>
</ul>
<p>Pomimo głębokich zmian, jakie zaszły w programie, dołożyliśmy wiele starań, by osoby korzystające z Pająka 2000 bez większego problemu mogły rozpocząć pracę w nowej wersji programu.</p>

<p>Pierwszą, najbardziej rzucająca się w oczy zmianą, był oczywiście nowy interfejs programu. Możliwe było dostosowywanie wyglądu programu w niespotykanym dotąd zakresie.</p>

<p>Dodane zostały nowe panele z zakładkami po prawej stronie i u dołu programu. Zawierają one nowe, potężne narzędzia. Klient FTP (znana z Pająka 2000 zakładka "Publikowanie") znajduje się teraz na prawym panelu i nosi nazwę "Moje serwery".</p>

<p>Zmianie uległy układ i zawartość menu głównego oraz pasków narzędzi, które teraz znacznie bardziej pomagają podczas pracy. Wszelkie narzędzia intuicyjnie znajdziesz właśnie tam, gdzie należy ich się spodziewać. Każde polecenie posiada własną ikonę, co znacznie ułatwia odnalezienie potrzebnego narzędzia wśród bogatej gamy innych poleceń.</p>

