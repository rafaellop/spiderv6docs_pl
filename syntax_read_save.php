<h1>Edytor schematów kolorowania - Wybór schematu</h1>

<!-- treść pomocy -->

W górnej części edytora schematów kolorowania znajdują się ikony do zapisu, odczytu i wyboru schematu spośród istniejących.

<p><b>Nowy schemat kolorowania</b><br />
Kliknij, jeśli chcesz utworzyć nowy schemat kolorowania. W przypadku, gdy aktualnie edytowany schemat ma niezachowane zmiany, zostaniesz spytany, czy chcesz go zapisać na dysk.</p>

<p><b>Lista schematów</b><br />
Z tej listy możesz wybrać schemat kolorowania, który chcesz edytować. Z wszystkich widocznych na tej liście schematów możesz korzystać w edytorze (w oknie Tworzenie).</p>

<p><b>Otwarcie schematu kolorowania z dysku</b><br />
Kliknij aby otworzyć z dysku schemat kolorowania niedostępny z poziomu listy (np. umieszczony w katalogu innym niż domyślny).</p>

<p><b>Zapisanie schematu na dysk</b><br />
Kliknij na tę ikonę, aby zachować zmiany w aktualnie edytowanym schemacie. Aby zapisać schemat pod nową nazwą, kliknij na ikonę 'Zapisanie schematu pod wskazaną nazwą'.</p>


