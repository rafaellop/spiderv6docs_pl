W górnej części edytora schematów kolorowania znajdują się ikony do zapisu, odczytu i wyboru schematu spośród istniejących.

<p><b>Nowy schemat kolorowania</b><br />
Kliknij, jeśli chcesz utworzyć nowy schemat kolorowania. W przypadku, gdy aktualnie edytowany schemat ma niezachowane zmiany, zostaniesz spytany, czy chcesz go zapisać na dysk.</p>

<p><b>Lista schematów</b><br />
