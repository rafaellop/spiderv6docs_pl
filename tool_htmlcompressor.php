<h1>Narzędzia - Kompresor HTML</h1>

<p>Za pomocą tego narzędzia możesz zmniejszyć rozmiar swojej strony, dzięki czemu będzie mogła być szybciej załadowana przez oglądających. Uważaj jednak, bo ta funkcja może uczynić kod Twojego dokumentu nieczytelnym, a wręcz niemożliwym do reedycji.

<p><b>Uwaga! Przed użyciem tego narzędzia proszę przetestować jego działanie na kopii dokumentu.</b>

<h2>Uruchamianie</h2>

<p>Narzędzie kompresora HTML możesz uruchomić za pomoca przycisku oznaczonego ikoną pokazaną powyżej. Przycisk uruchomienia tego okna znajduje się w menu <a href="main_menu_tools.php">Narzędzia</a>/<a href="main_menu_tools_formatting.php">Formatowanie i kompresja kodu</a>.

<h2>Źródło kompresji</h2>

<p>Kompresji możesz dokonywać na aktualnym dokumencie, wszystkich otwartych dokumentach lub zaznaczonym w edytorze tekście. Wybierz odpowiednią opcję.

<h2>Ustawienia kompresji</h2>

<p>Dostępne są następujące opcje kompresji, które powodują usuwanie zbędnych elementów z treści kodu źródłowego strony.
Zaznacz te opcje, które powinny być uwzględnione podczas procesu kompresji. Uważaj zwłaszcza na ostatnią opcję, która może spowodować, że dokument stanie się całkowicie nieczytelny ze względu na pozbawienie go wszystkich zakończeń linii.

<p><b>Zbędne spacje</b><br>
Usuwa zbędne spacje np. podwójne, potrójne, etc.

<p><b>Wszystkie tabulatory</b><br>
Usuwa znaki tabulatora

<p><b>Wszystkie komentarze</b><br>
Uwaga, niebezpieczne dla skryptów i stylów!

<p><b>Znacznik &lt;!--webbot---&gt;</b><br>
Pozostałość po FrontPage

<p><b>Puste linie</b><br>
Usuwa niepotrzebne znaki końca linii

<p><b>Zbędne łamanie linii</b><br>
Usuwa niepotrzebne znaki końca linii

<p><b>Wszystkie znaki końca linii</b><br>
Uwaga! powoduje brak czytelności kodu!

<h2>Alternatywa</h2>

<p>Alternatywą dla kompresora jest narzędzie formatowania dokumentów znacznikowych, które przychodzi z predefiniowanym ustawieniem o nazwie "Kompresja kodu". <a href="formatter_general.php">Formatowania</a> pozwalają również na uzyskanie odwrotnego efektu do kompresji, tzn. ładnie i czytelnie układają kod.

