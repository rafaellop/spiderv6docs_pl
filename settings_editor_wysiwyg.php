<h1>Ustawienia programu - Edycja wizualna</h1>

<!-- treść pomocy -->
<p>
Ta kategoria pozwala na określenie podstawowych ustawień edycji wizualnej, czyli w trybie graficznym, a nie z widokiem kodu źródłowego. Znajdują się tutaj dwie grupy ustawień w których masz wpływ na funkcję edycji wizualnej za pomocą ustawień:

<p><b>Edytuj nowo otwierane dokumenty w trybie wizualnym</b><br>
Zaznacz tę opcję, aby nowootwierane dokumenty były edytowane w trybie wizualnym zamiast w edytorze kodu.

<p><b>Pokazuj ikonki zastępujące niektóre niewidoczne znaczniki</b><br>
Zaznacz tę opcję, aby podczas edycji w trybie wizualnym, niektóre niewidoczne znaczniki były reprezentowane za pomocą specjalnych ikon.

<p><b>Wyświetlaj niewidoczne obramowania tabel</b><br>
Zaznacz tę opcję, aby w trybie edycji wizualnej widoczne były obramowania tabelek, które podczas przeglądania będą niewidoczne.

<p><b>Oczyszczanie kodu źródłowego</b><br />
Zaznacz tę opcję, aby kod źródłowy dokumentu został oczyszczony za pomocą Tidy przy przejściu z edycji w trybie wizualnym do pracy z kodem źródłowym.</p>

