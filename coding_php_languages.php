<h1>Wspomaganie dla PHP - Zakładka "Języki programowania"</h1>



<!-- treść pomocy -->



<p>Jeśli nie pamiętasz do czego służy dana funkcja PHP, bądź szukasz funkcji odpowiedniej do wykonania określonego zadania, bardzo przydatna jest  zakładka <a href="main_tabs_languages.php">Języki programowania</a>. </p>

<h2>Opis narzędzia</h2>

<p>Zakładka ta składa się z menu, w którym możesz przełączać języki programowania oraz drzewka z danymi. Jeśli wybierzesz widok dla PHP na drzewku znajdą się gałęzie:</p>

<p><b>Stałe i zmienne:</b>
<ul>
   <li><b>Stałe PHP</b> - lista nazw stałych predefiniowanych przez PHP (np. w pliku <tt>php.ini</tt>)</li>
   <li><b>Stałe serwera (Server)</b> - lista nazw stałych serwera dotyczących samego serwera</li>
   <li><b>Stałe serwera (Request)</b> - lista nazw stałych serwera dotyczących obłsugi przez serwer zapytania od przeglądarki</li>
   <li><b>Zmienne HP</b> - lista nazw zmiennych PHP, np. tablica argumentów funkcji, zmienne sesji itp.</li>
</ul></p>

<p><b>Funkcje</b><br />
Zawiera listy nazw wbudowanych funkcji PHP. Zostały one pogrupowane zgodnie z ich przeznaczeniem, lub modułem który ich dostarcza.</p>



<p>Opisywana zakładka może być np. ściągawką. Nie łatwo bowiem spamiętać funkcje, stałe i zmienne PHP - w samym podręczniku opisanych grubo ponad 2000, cały czas dochodzą nowe. Dla wybieranych na drzewku elementów, w dolnej części zakładki wyświetlany jest obszar podpowiedzi udostępniający więcej informacji o wybranej funkcji. Na przykład dla <tt>MySQL/mysql_fetch_row()</tt> uzyskamy podpowiedź:

<p>
<tt>
<b>Nazwa:</b> <i>mysql_fetch_row()</i><br>
<b>Argumenty:</b> <i>object result</i><br>
<b>Typ:</b> array<br>
<b>Obsługa:</b> PHP 3, PHP 4<br>
</tt>



<p>Z tej zakładki możesz nie tylko korzystać jako ze ściągawki. Możesz również wstawiać wybrane elementy do dokumentu. Zrobisz to na dwa sposoby: za pomocą przycisku w lewej części zakładki lub po dwukliku na wybranym elemencie. Pająk przy tym ostrzeże jeśli będziesz chciał umieścić element w nieodpowiednim miejscu. </p>



