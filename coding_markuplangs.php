<h1>Wspomaganie dla języków znacznikowych</h1>



<!-- treść pomocy -->

<p>Język HTML (i jego następca - XHTML) jest najważniejszym językiem znacznikowym stosowanym do tworzenia stron. Jednak oprócz niego istnieje znacznie więcej języków znacznikowych. Pająk wspomaga najważniejsze (pod kątem wykorzystania w Sieci) z nich :

<ul>                                                                                                                                                                                                                                   
	<li><b>WML</b> - (ang. <i>Wireless Markup Language</i>) język opisu stron, a których korzystają urządzenia przenośne (przede wszystkim telefony komórkowe) za pomocą protokołu WAP (ang. <i>Wireless Application Protocol</i>)</li>
	<li><b>SVG</b> - (ang. <i>Scalable Vector Graphics</i>) - alternatywa dla animacji Flash</li>
	<li><b>MathML</b> - (ang. <i>Mathematical Markup Language</i>) - język służący do przedstawiania symboli i wyrażeń matematycznych</li>
	<li><b>SMIL</b> - (ang. <i>Synchronized Multimedia Integration Language</i>) - język wspomagający tworzenie prezentacji multimedialnych</li>
	<li><b>XML</b> - (ang. <i>eXtensible Markup Language</i>) - uniwersalny, coraz bardziej popularny język opisu dokumentu</li>
	<li><b>RSS</b> - (ang. <i>Really Simple Syndication 2.0</i>) - język służący do rozpowszechniania newsów itp.</li> 
	<li><b>P3P</b> - (ang. <i>Platform for Privacy Preferences 1.0</i>)</li>
   <li><b>Atom</b> - nowy sposób rozpowszechniania newsów i innych informacji na stronach www.</li>
</ul>

<p>Informacje na temat języka, jego znaczników, atrybutów itd. Pająk pobiera ze specjalnych plików definicyjnych dla języków znacznikowych - TDF. Do edycji danych służy specjalne narzędzie - <a href="tagdata_editor_tdf.php">Edytor plików definicyjnych języków znacznikowych</a>. Korzystając z niego możesz również dodawać i usuwać obsługiwane języki znacznikowe. Dzięki temu Pająk jest wszechstronnym i bardzo elastycznym edytorem, doskonałym do tworzenia dokumentów w dowolnym języka znacznikowym.</p>

<p>Z uwagi na przeznaczenie Pająka do tworzenia przede wszystkim stron internetowych, wszystko co dotyczy tylko języka HTML zostało ujęte w temacie <a href="coding_html.php">Wspomaganie dla języka HTML</a>.</p>

<p>Jednakże wszystko, o czym przeczytasz w tym temacie pomocy, dotyczy zarówno języka HTML i XHTML, jak i pozostałych wyżej wymienionych. </p>

<p>Wspomaganie dla języków znacznikowych obejmuje przede wszystkim narzędzia:
<ul>
	<li><a href="coding_markuplangs_syntax.php">Kolorowanie składni</a></li>
