<h1>Edytor schematów kolorowania - Podgląd schematu kolorowania</h1>

<!-- treść pomocy -->

W oknie podglądu można śledzić na bieżąco wpływ zmian w ustawieniach schematu na kolorowanie tekstu. Przykładowy tekst podglądu można zmieniać za pomocą przycisków z prawej strony okna.

<p><b>Użyj domyślnego tekst podglądu</b><br />
Kliknij tu, aby włączyć tekst podglądu zapisany w aktualnym schemacie kolorowania. Jest to ustawienie domyślne.</p>

<p><b>Użyj bieżącego dokumentu jako tekst podglądu</b><br />
Kliknij tu, aby wczytać do okienka podglądu dokument aktualnie otwarty w edytorze. Jeśli żaden dokument nie jest otwarty, ikona pozostaje nieaktywna. <br />
<b>Uwaga!</b> Jeśli podczas zapisu schematu kolorowania ta opcja będzie aktywna, tekst skopiowany z bieżącego dokumentu zastąpi dotychczasowy domyślny tekst podglądu. Zmiany w tekście podglądu nie mają wpływu na dokument otwarty w edytorze!</p>

<p><b>Użyj wskazanego dokumentu jako tekst podglądu</b><br />
Kliknij tu, aby wczytać z dysku dokument, który zostanie wyświetlony jako tekst podglądu.<br />
<b>Uwaga!</b> Jeśli podczas zapisu schematu kolorowania ta opcja będzie aktywna, tekst z wczytanego dokumentu zastąpi dotychczasowy domyślny tekst podglądu. Zmiany w tekście podglądu nie mają wpływu na wczytany dokument!</p>

<b>Umożliwiaj modyfikację tekstu podglądu</b><br />
Umożliwia ręczne wprowadzanie zmian w tekście podglądu. Opcja domyślnie jest wyłączona, co zapobiega nieumyślnemu wprowadzeniu zmian.


