<h1>Ustawienia programu - Skróty klawiszowe</h1>




<!-- treść pomocy -->
<p>
W tej kategorii znajdują się narzędzia pozwalające na modyfikację głównych skrótów klawiszowych dla akcji powiązanych z menu głównym oraz paskami narzędzi. Nie możesz w tym miejscu ustawiać skrótów klawiszowych dla makrokodów, bowiem te skróty przypisywane są dla każdego makrokodu osobno w oknie <a href="macrocodes_editor.html">dodawania lub modyfikacji makrokodu</a>.

<p>
Ta kategoria składa się z listy wszystkich dostępnych w programie akcji/poleceń, których nazwy znajdują się w pierwszej kolumnie listy. Dwie następne kolumny wyświetlają przypisany do polecenia skrót główny oraz skrót dodatkowy, alternatywny. Poniżej listy znajdziesz miejsce na podanie nowego skrótu dla wybranego polecenia.

<h2>Modyfikacja skrótu klawiszowego</h2>

<p>Aby zmodyfikować lub przypisać skrót klawiszowy do wybranego polecenia, wybierz polecenie na liście, a następnie przejdź do pola "Główny skrót klawiszowy". W tym polu wciśnij skrót klawiszowy, który chcesz zdefiniować dla wybranego polecenia, a następnie przypisz go za pomocą przycisku ze strzałką. Możesz też przypisać dodatkowy skrót dla wybranego polecenia. Zrobisz to w polu poniżej. Po wciśnięciu przycisku oznaczonego strzałką skrót zostanie przypisany do polecenia i pojawi się w odpowiedniej kolumnie na liście skrótów klawiszowych.

<p><b>Eksportuj przypisane skróty do pliku HTML</b><br>
Zapisuje bieżącą listę skrótów klawiszowych w postaci pliku HTML.

