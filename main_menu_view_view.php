<h1>Menu główne - Wyświetl - Widok</h1>

<!-- Treść pomocy -->

<p>Za pomocą tego menu możesz zmieniać wygląd głównego okna programu, a więc włączać widok paneli, pasków narzędzi, itp:</p>

<p><b>Paski narzędzi</b><br />
Podmenu zawierające przełączniki pozwalające na wybór <a href="toolbars_general.php">pasków narzędzi</a> widocznych w głównym oknie programu.

<p><b>Panel zasobów z lewej</b><br />
Podmenu zawierające przełączniki pozwalające na uaktywnienie jednej z zakładek <a href="main_tabs_general.php#tabs_leftpanel">panelu znajdującego się po lewej</a> stronie w głównym oknie programu. Domyślny skrót klawiszowy to <tt>F9</tt>

<p><b>Panel zasobów z prawej</b><br />
Podmenu zawierające przełączniki pozwalające na uaktywnienie jednej z zakładek <a href="main_tabs_general.php#tabs_rightpanel">panelu znajdującego się po prawej</a> stronie w głównym oknie programu. Domyślny skrót klawiszowy to <tt>Ctrl+F9</tt>

<p><b>Panel wyników</b><br />
Podmenu zawierające przełączniki pozwalające na uaktywnienie jednej z zakładek <a href="down_panel.php">panelu znajdującego się na dole</a> głównym oknie programu. Domyślny skrót klawiszowy to <tt>Lewy Alt+F9</tt>

<p><b>Pasek stanu</b><br />
Kliknij, aby włączyć lub wyłączyć widok paska stanu na dole głównego okna programu.

<p><b>Pełny ekran</b><br />
To polecenie pozwala włączyć lub wyłączyć tryb pełnoekranowy, podczas którego widoczny jest jedynie obszar edycji bądź podglądu, mini pasek oraz menu. Domyślny skrót klawiszowy to <tt>Shift+F9</tt>.

<hr />
<p>Trzy następne polecenia działają podobnie jak analogiczne przyciski w prawym górnym rogu okna programu. Jednak dzięki temu, że są umieszczone w menu programu, można teraz do nich łatwo przypisać skróty klawiszowe .</p>

<p><b>Maksymalizuj</b><br />
Kliknij, aby zmaksymalizować okno programu. 

<p><b>Minimalizuj</b><br />
Kliknij, aby zminimalizować okno programu. 

<p><b>Przywróć</b><br />
Kliknij, aby przywrócić na ekran zminimalizowane okno programu. 

<hr />

<p><b>Tryb pracy głównego okna</b><br />
Kliknij, aby uruchomić narzędzie, które pozwoli w łatwy sposób wybrać preferowany tryb pracy programu. Opis narzędzia znajduje się w <a href="tool_welcometo.php">osobnym rozdziale</a>.

