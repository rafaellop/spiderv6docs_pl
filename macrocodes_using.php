<h1>Szablony dostarczone z programem</h1>

<!-- treść pomocy -->


<p>Razem z programem dostarczonych zostały setki szablonów. Są to różne typy szablonów - od prostych, które zawierają nieskomplikowane konstrukcje ułatwiające pracę w językach programowania (np. szkielety funkcji itp.), na kompletnych szablonach dokumentów zawierających grafiki itp. skończywszy.</p>

<p>Szablony zostały pogrupowane w postaci gałęzi drzewka. Wśród nich znajdziesz m. in. gałęzie zawierające:</p>

<ul>
	<li><b><a href="#code_templates">Szablony kodu</a></b> - Szablony konstrukcji kodu stosowanych w różnych językach programowania</li>
	<li><b><a href="#doc_templates">Szablony nowych</a></b> - Szablony nowych dokumentów.</li>
	<li><b><a href="#service_templates">Szablony stron</a></b> - Szablony gotowych stron www wraz z grafikami itd.</li>
	<li><b><a href="#special_chars">Znaki specjalne</a></b> - Zestawy znaków matematycznych greckich, dekoracyjnych itp.</li>
</ul>

<a name="doc_templates"></a>
<h2>Szablony nowych</h2>
W tej gałęzi znajdują się szablony nowych dokumentów umożliwiające szybkie stworzenie szkieletu strony, arkusza stylów itp.


<a name="code_templates"></a>
<h2>Szablony kodu</h2>

W tej grupie znajdują się szablony kodu zawierające najczęściej używane konstrukcje w językach programowania, jak pętle, instrukcje warunkowe itp. Zawiera następujące grupy szablonów:


<ul>
	<li><b>JavaScript</b> - Szablony kodu wspomagające pracę w JavaScript</li>
	<li><b>PHP</b> - Szablony kodu wspomagające pracę w PHP</li>
	<li><b>VBScript</b> - Szablony kodu wspomagające pracę w VBScript</li>
</ul>


<a name="service_templates"></a>
<h2>Szablony stron</h2>

<p>W tej gałęzi znajdują się szablony, za których pomocą możesz w łatwy sposób utworzyć gotową stronę wraz z grafikami, wbudowanymi arkuszami stylów itp.</p>

<p>Podczas tworzenia z szablonu gotowego dokumentu odpowiednie grafiki będą się automatycznie kopiowały w miejsce, w którym będziesz zapisywać dokument.</p>

<a name="special_chars"></a>
<h2>Znaki specjalne</h2>

<p>W tej gałęzi umieszczone zostały szablony zawierające znaki specjalne, których nie można bezpośrednio użyć. Zamiast tego każdy znak przedstawiony jest za pomocą encji (ang. <i>entity</i>).</p>

<p>Na przykład znak <tt>&amp;</tt> zapisany jest jako encja <tt>&amp;amp;</tt>.</p>

Znaki specjalne pogrupowane zostały w gałęziach:

<ul>
	<li><b>Alfabet grecki</b> - wielkie i małe litery alfabetu greckiego,</li>
	<li><b>Dekoracyjne</b> - znaki karo, kier, pik, trefl,</li>
	<li><b>Litery</b> - litery spoza ASCII, np. <i>C cedilla</i>, czyli <tt>&Ccedil;</tt>,</li>
	<li><b>Znaki</b> - różne znaki sposa ASCII, np. pokazany wyżej <tt>&amp;</tt></li>
	<li><b>Znaki matematyczne</b> - różne znaki matematyczne, np. <i>nieskończoność</i> (ang. <tt>infinity</tt>), czyli &infin;</li>
</ul>

