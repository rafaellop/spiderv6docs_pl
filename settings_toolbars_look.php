<h1>Ustawienia programu - Wygląd pasków i menu</h1>

<!-- treść pomocy -->
<p>
   W tej kategorii znajdziesz ustawienia odpowiadające za wygląd pasków narzędzi oraz menu programu. Możesz w znajdujących się tutaj grupach określać zarówno kolorystykę jak i styl menu i pasków. Oto poszczególne grupy i ich ustawienia:

<h2>Wygląd i kolorystyka pasków w stylu</h2> 

<p>W tej grupie wybierz tę opcję, która odpowiada preferowanej kolorystyce i wyglądowi pasków i menu.

<h2>Dla wybranego stylu używaj kolorów z zestawu</h2> 

<p>Jeśli preferujesz jeden styl wyglądu pasków, ale kolorystykę innego rodzaju, tutaj wybierz paletę, której chcesz używać.

<h2>Ustawienia</h2> 

<p><b>Cień w menu</b><br>
Zaznacz tę opcję, jeśli chcesz, aby wokół menu rysowany był dodatkowy cień.

<p><b>Gradient pod aktywną pozycją menu</b><br>
Zaznacz tę opcję, jeśli chcesz, aby tło pozycji menu, nad którym znajduje się myszka, było stworzone za pomocą przejścia kolorów.

<p><b>Nieaktywne obrazki w jaśniejszym odcieniu</b><br>
Zaznacz tę opcję, jeśli chcesz, aby obrazki ikon przycisków nieaktywnych były rysowane w delikatnie jaśniejszym odcieniu.

<p><b>Pokaż strzałki obok nazw w menu głównym</b><br>
Zaznacz tę opcję, jeśli chcesz, aby obok nazw w menu głównym pojawiły się strzałki. Menu będzie nieco bardziej czytelne, lecz zajmie więcej miejsca na ekranie.

<p><b>Ikony w odcieniach szarości gdy nieaktywne</b><br>
Zaznacz tę opcję, jeśli chcesz, aby obrazki ikon przycisków nieaktywnych były rysowane w odcieniach szarości.

<p><b>Zawijaj przyciski na pasku zamiast wyświetlać strzałkę</b><br />
Jeśli pasek narzędzi zawiera więcej przycisków niż można wyświetlić na aktualnym panelu, gdzie znajduje się pasek, przyciski będą w zależności od tego ustawienia, wyświetlane w dodatkowym podmenu dostępnym pod przyciskiem strzałki lub cały pasek narzędzi zostanie "zawinięty", aby wszystkie przyciski były widoczne na ekranie

<h2>Styl cienia menu</h2>

<p>Jeśli wybrałeś cień dla menu w jednej z opcji powyżej, tutaj możesz określić w jakim stylu będzie ono wyświetlane.

<h2>Gradient pod aktywną pozycją w menu</h2>

<p>Jeśli wybrałeś gradient dla aktywnych pozycji menu w jednej z opcji powyżej, możesz tutaj określić w jaki sposób będzie rysowane przejście kolorów: od lewej do prawej, czy z góry na dół.

<h2>Gradient w menu</h2>

<p>Jeśli chcesz, aby tło całego menu było rysowane z użyciem przejścia kolorów, zaznacz wybraną tutaj pozycję określającą styl tego przejścia.

<h2>Gradient pod obrazkami w menu</h2>

<p>Jeśli chcesz, aby pod obrazkami w menu również znalazło się przejście kolorystyczne na tle, zaznacz wybraną tutaj pozycję określającą styl tego przejścia.

