<p>Definicje języków programowania przedstawione są w postaci drzewka, które posiada odpowiednie kategorie związane albo z typem obiektu, którego metody i własności zawierają, albo z kategoriami (np. w PHP) tworzonymi w edytorze pliku definicyjnego.

<p>Opisywana zakładka może być np. ściągawką. Nie łatwo bowiem spamiętać funkcje czy własności dla tych wszystkich języków programowania, które coraz szerzej wchodzą w technologie sieciowe. Dla wybieranych na drzewku elementów, w dolnej części zakładki wyświetlany jest obszar podpowiedzi udostępniający więcej informacji o wybranym elemencie. Na przykład dla <tt>window/location</tt> uzyskamy podpowiedź:

<p>
<tt>
<b>Nazwa:</b> location<br>
<b>Opis:</b> Adres URL zawartości wyświetlanej w oknie<br>
<b>Typ:</b> Location<br>
<b>Obsługa:</b> JavaScript >= 1.0<br>
</tt>

<p>Jak widać informacje prezentowane w tym obszarze są dość przydatne.

<p>Z tej zakładki możesz nie tylko korzystać jako ze ściągawki. Możesz również wstawiać wybrane elementy do dokumentu. Zrobisz to na dwa sposoby: za pomocą przycisku w lewej części zakładki "Języki programowania" lub po dwukliku na wybranym elemencie. Pająk przy tym ostrzeże jeśli będziesz chciał umieścić element w nieodpowiednim miejscu.

<p>Jeśli wybierzesz widok języków znacznikowych, będziesz mógł z rozwijanego menu wybrać ten język, którego znaczniki, atrybuty i zdarzenia chciałbyś zobaczyć na drzewku.
