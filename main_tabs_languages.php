<h1>Zakładka "Języki programowania"</h1>



<p>Zakładka "Języki programowania" zawiera zestaw definicji dla kilku języków programowania wspieranych przez Pająka. Włączysz ją za pomocą panelu po prawej lub skrótu klawiszowego CTRL+8, a także z menu pod przyciskiem pokazanym powyżej czy z menu głównego. Dostępne tutaj języki to:

<p>
