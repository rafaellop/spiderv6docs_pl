<h1>Paski narzędzi - Główny pasek</h1>

<!-- Treść pomocy -->

<p>Pasek ten zawiera często używane polecenia służące do obsługi programu.</p>

<p><b><a href="main_menu_file_new.php">Nowy dokument</a></b><br />
Kliknij, aby utworzyć nowy, pusty dokument. Klikając na strzałkę z prawej, możesz skorzystać z podmenu w celu uruchomienia dodatkowych narzędzi.</p>


<p><b><a href="tool_importer.php">Importuj...</a></b><br />
Kliknij, aby otworzyć Importer plików.</p>

<p><b>Otwórz</b><br />
Kliknij aby otworzyć istniejący dokument. Klikając na strzałkę z prawej, możesz skorzystać z listy ostatnio otwieranych dokumentów.</p>

<p><b>Zapisz</b><br />
Kliknij, aby zapisać bieżący dokument.</p>

<p><b>Drukuj...</b><br />
Kliknij, aby otworzyć okienko drukowania.

<p><b>Cofnij</b><br />
Kliknij, aby cofnąć ostatnią operację w edytorze (domyślny skrót <tt>Ctrl+Z</tt>).

<p><b>Ponów</b><br />
kliknij, aby ponownie wykonać operację cofniętą w edytorze (domyślny skrót <tt>Shift+Ctrl+Z</tt>).

<p><b>Wytnij</b><br />
Kliknij, aby wyciąć zaznaczenie do schowka (domyślny skrót <tt>Ctrl+X</tt>).

<p><b>Kopiuj</b><br />
Kliknij, aby skopiować zaznaczenie do schowka (domyślny skrót <tt>Ctrl+C</tt>).

<p><b>Wklej</b><br />
Kliknij, aby wkleić zawartość schowka (domyślny skrót <tt>Ctrl+V</tt>).

<p><b>Panel zasobów z lewej</b><br />
Podmenu zawierające przełączniki pozwalające na uaktywnienie jednej z zakładek <a href="main_tabs_general.php#tabs_leftpanel">panelu znajdującego się po lewej</a> stronie w głównym oknie programu. Domyślny skrót klawiszowy to <tt>F9</tt>

<p><b>Pełny ekran</b><br />
To polecenie pozwala włączyć lub wyłączyć tryb pełnoekranowy, podczas którego widoczny jest jedynie obszar edycji bądź podglądu, mini pasek oraz menu. Domyślny skrót klawiszowy to <tt>Shift+F9</tt>.

<p><b>Panel zasobów z prawej</b><br />
Podmenu zawierające przełączniki pozwalające na uaktywnienie jednej z zakładek <a href="main_tabs_general.php#tabs_rightpanel">panelu znajdującego się po prawej</a> stronie w głównym oknie programu. Domyślny skrót klawiszowy to <tt>Ctrl+F9</tt>

<p><b>Ustawienia programu</b><br />
Kliknij, aby zmienić bieżące ustawienia programu otwierając okno <a href="settings.php">ustawień programu</a>. Podmenu dostępne po kliknięciu strzałki rozwija menu poleceń dotyczących ustawień (identyczne jak <a href="main_menu_view_settings.php">Menu główne / Wyświetl / Ustawienia</a>).

<p><b>Podgląd dokumentu</b><br />
Kliknij ten przycisk, aby wykonać podgląd bieżącego dokumentu w domyślnej przeglądarce. Domyślną przeglądarkę możesz określić w <a href="settings_browse.php">Ustawieniach przeglądania</a>.Jeśli klikniesz strzałkę obok przycisku, będziesz mógł wybrać z menu dodatkowe przeglądarki, które definiuje się również we wspomnianym wyżej oknie ustawień. Dzięki temu możesz wywołać podgląd bieżącego dokumentu w innych przeglądarkach niż ta domyślnie wybrana. Menu pod przyciskiem jest identyczne, jak <a href="main_menu_main.php">Menu główne</a> / <a href="main_menu_view.php">Wyświetl</a> / Podgląd.

