<h1>Narzędzia - Edytor tytułu dokumentu i sekcji META</h1>

<p>Sekcja META dokumentów HTML jest bardzo ważną sekcją, zwłaszcza w polskich warunkach. W sekcji tej znajdują się bowiem bardziej szczegółowe informacje na temat zawartości dokumentu HTML, np. używana strona kodowa dla znaków narodowych, itp. Edytor sekcji META pozwala na łatwe określenie parametrów sekcji META w wygodny sposób.

<p>Okno edytora sekcji META uruchomisz z poziomu <a href="main_menu_main.php">Menu głównego</a> / <a href="main_menu_tools.php">Narzędzia</a> / <a href="main_menu_tools_structure.php">Struktura</a> / Edytor sekcji &lt;meta&gt;, a także z pasków narzędzi i skrótem klawiszowym <tt>Shift+Ctrl+M</tt>. Dodatkowo możliwe jest określenie domyślnych właściwości sekcji META dla każdego Serwisu z osobna. Odpowiedni przycisk uruchomiający to okno znajdziesz więc w oknie <a href="service_properites.php#servicepropertiesdocuments">Właściwości serwisu / Dokumenty</a>. Ikona polecenia przedstawiona została powyżej.

<h2>Ogólne informacje</h2>

<p>Należy w tym miejscu przypomnieć, że polski standard kodowania znaków ustala norma <tt>ISO-8859-2</tt>, która powinna być przestrzegana (więcej o polskich znakach można przeczytać na <a href="https://www.ogonki.agh.edu.pl/" target="_blank">Polskiej Stronie Ogonkowej</a>).

<p>Oprócz informacji o używanej stronie kodowej, w sekcji META zawarte są dane pomocne przy katalogowaniu stron przez serwisy wyszukiwawcze (słowa kluczowe, opis strony, język dokumentu) oraz inne (autor, użyty program narzędziowy, itp.). Można także określić, czy strona ma być przechowywana w pamięci tymczasowej (cache) przeglądarek.

<p>Edytor sekcji META umożliwia szybkie zdefiniowanie wszystkich tych parametrów. Zalecane więc jest umieszczanie sekcji META w każdym tworzonym dokumencie, chociażby tylko z podstawowymi danymi.

<p>W okienku edytora znajdziesz kilka zakładek, których opis przedstawiamy poniżej:



<h2>Zakładka Ustawienia ogólne</h2>

<p>Na tej zakładce masz dostęp do podstawowych i najważniejszych ustawień sekcji META. Znajdziesz tutaj następujące ustawienia:

<p><b>Tytuł dokumentu</b><br>
Wypełnij to pole, jeśli chcesz wprowadzić tytuł dla strony. Zostanie on umieszczony w znaczniku &lg;TITLE&gt;, a wyświetlany będzie na pasku tytułowym przeglądarki.

<p><b>Autor</b><br>
Zaznacz ten przełącznik i wypełnij pole obok, jeśli chcesz w sekcji META strony zawrzeć informacje o autorze.

<p><b>Język</b><br>
Zaznacz ten przełącznik i wybierz język z pola obok, jeśli chcesz w sekcji META strony umieścić informację o używanym na stronie języku.

<p><b>E-mail</b>, czyli adres email autora<br>
Zaznacz ten przełącznik i wprowadź swój adres email w pole obok, jeśli chcesz w sekcji META strony umieścić adres email autora dokumentu.

<p><b>Opis strony</b><br>
Zaznacz ten przełącznik i wprowadź tekst opisu strony obok.

<p><b>Kodowanie znaków w dokumencie</b><br>
Zaznacz ten przełącznik jeśli chcesz ustalić kodowanie znaków narodowych w dokumencie. Kodowanie wybierzesz przyciskiem obok.

<h3>Odświeżanie</h3>

<p>W tej grupie znajdziesz kilka ustawień, który umożliwiają określenie sposobu automatycznego odświeżania strony w przeglądarce:</p>

<p><b>Odśwież stronę co</b> pewien czas<br>
Zaznacz ten przełącznik jeśli chcesz spowodować, aby strona automatycznie ładowała się na nowo po określonym czasie. Czas możesz ustalić obok.

<p><b>... i przejdź pod adres</b><br>
Jeśli chcesz, aby po określonym wyżej czasie strona odświeżyła się, ale załadowała inny adres, zaznacz ten przełącznik i wprowadź w polu poniżej nowy adres. Inaczej mówiąc, w ten sposób możesz stworzyć przekierowanie.

<h3>Dodatkowe</h3>

<p>Ostatnia grupa zakładki zawiera opcje umożliwiające umieszczenie w sekcji META dodatkowych informacji, jak:</p>

<p><b>Dodaj datę utworzenia</b><br>
Zaznacz ten przełącznik jeśli chcesz umieścić w sekcji META dokumentu datę jego utworzenia.

<p><b>Dodaj informację o programie</b><br>
Zaznacz ten przełącznik jeśli chcesz umieścić w sekcji META informację o programie użytym do jej utworzenia.

<p><b>Nie przechowuj w cache</b><br>
Zaznacz ten przełącznik jeśli chcesz zablokować przechowywanie tej strony przez przeglądarki w ich pamięci podręcznej. Powoduje to, że Twoja strona będzie zawsze odczytywana z serwera i nigdy z cache przeglądarki.

<p><b>Nie pokazuj paska narzędzi obrazu</b><br />
Zaznacz ten przełącznik jeśli chcesz zablokować pokazywanie paska narzędzi obrazu dla wszystkich grafik umieszczonych na tej stronie.<br>
Domyślnie pasek ten pojawia się jedynie w przeglądarce Internet Explorer, dla obrazków większych niż 130x130 pikseli.

<p><b>Dołącz transformację</b><br>
Zaznacz ten przełącznik jeśli chcesz zdefiniować efekty przekształceń przy wchodzeniu lub opuszczaniu strony. Efekty transformacji działają wyłącznie w MS Internet Explorerze. Przekształcenia ustala się na zakładce <a href="#transformation">Efekty przekształceń</a>.







<a name="keywords"></a>
<h2>Indeksowanie oraz słowa kluczowe</h2>

<p>Aby wspomóc wyszukiwarki internetowe (np. <a href="https://www.google.pl" target="_blank">Google</a>) w odnajdywaniu dokumentu w sieci, na tej zakładce możesz ustalić parametry pomocne przy identyfikacji dokumentu przez programy wyszukiwarek (ang. <i>robots</i>).

<p>Grupa o nazwie <b>Sposób indeksowania przez roboty</b> zawiera przełączniki pozwalające na określenie reguł indeksowania dokumentu przez programy wyszukiwarek. Nazwy przełączników są dobrym wyjaśnieniem ich działania.

<p>Grupa o nazwie <b>Słowa kluczowe</b> pozwala na tworzenie listy tzw. słów kluczowych (ang. <i>keywords</i>), które przez niektóre wyszukiwarki używane są do indeksowania stron. Lista słów kluczowych uaktywnia się po zaznaczeniu przełącznika w nazwie grupy. Kolejne słowa kluczowe możesz możesz dodawać, zmieniać ich kolejność i sortować za pomocą przycisku obok listy.

<p><b><a href="tool_keywords_generator.php">Generator słów kluczowych</a></b><br />
To narzędzie umożliwia szybkie wygenerowanie listy słów wystęujących w bieżącym dokumencie.



<a name="transformation"></a>
<h2>Efekty przekształceń</h2>

<p>Efekty przekształceń pozwalają na określenie efektów specjalnych wyświetlanych przy wchodzeniu lub opuszczaniu strony. Efekty te działają tylko w MS Internet Explorer i warto o tym pamiętać. Na zakładce znajdują się następujące ustawienia:

<p><b>Efekt przekształcenia przy wchodzeniu na stronę</b><br>
Zaznacz ten przełącznik, jeśli chcesz ustalić efekt przekształcenia podczas wchodzenia na tę stronę.

<p><b>Lista efektów przekształceń</b><br>
Wybierz efekt przekształcenia z listy i określ jego parametry. Następnie możesz wykonać podgląd za pomocą przycisku obok. Lista dostępnych efektów przedstawia się następująco:

<ol>
	<li>Kwadrat do środka
	<li>Kwadrat do zewnątrz
	<li>Koło do środka
	<li>Koło na zewnątrz
	<li>Pionowe przesunięcie w górę
	<li>Pionowe przesunięcie w dół
	<li>Poziome przesunięcie w prawo
	<li>Poziome przesunięcie w lewo
	<li>Pionowe pasy
	<li>Poziome pasy
	<li>Poziome i pionowe pasy w prawo
	<li>Poziome i pionowe pasy w dół
	<li>Pikselizacja
	<li>Poziome przesunięcie do środka
	<li>Poziome przesunięcie na zewnątrz
	<li>Pionowe przesunięcie do środka
	<li>Pionowe przesunięcie na zewnątrz
	<li>Po ukośnej z prawego rogu w dół
	<li>Po ukośnej z prawego rogu w górę
	<li>Po ukośnej z lewego rogu w dół
	<li>Po ukośnej z prawego rogu w górę
	<li>Poziome paski
	<li>Pionowe paski
	<li>Losowo wybrany efekt
</ol>

<p><b>Czas trwania efektu</b><br>
Wprowadź tutaj wartość określającą długość czasu przez który będzie trwał efekt.

<p><b>Efekt przekształcenia przy opuszczaniu strony</b><br>
Zaznacz ten przełącznik, jeśli chcesz ustalić efekt przekształcenia podczas opuszczania tej strony.

<p><b>Lista efektów przekształceń</b><br>
Wybierz efekt przekształcenia z listy i określ jego parametry. Następnie możesz wykonać podgląd.

<p><b>Czas trwania efektu</b><br>
Wprowadź tutaj wartość określającą długość czasu przez który będzie trwał efekt.

<p><b>Podgląd efektu</b><br>
Obszar wyświetlający podgląd wybranego efektu. Aby zobaczyć podgląd wciśnij przycisk oznaczony lupą, który uruchomi podgląd.

<a name="classification"></a>
<h2>Klasyfikacja zawartości</h2>

<p>Ta zakładka umożliwia szybkie sklasyfikowanie dokumentu za pomocą systemu RSACi. Wystarczy tylko zaznaczyć przełącznik "<b>Dołącz klasyfikację</b>" i ustalić odpowiednie parametry. Parametry ustalasz za pomocą odpowiednich suwaków wskazujących poziom obecności określonych treści na stronie. Dodatkowo możesz wskazać adres klasyfikującego, adres klasyfikowanej strony oraz datę ważności klasyfikacji. Jeśli natomiast chcesz, aby klasyfikacja była ważna tylko w obrębie tej strony, a nie np. całego serwisu, zaznacz przełącznik "<b>Klasyfikacja ważna tylko w obrębie tej strony</b>".

<h2>Przycisk Przypisz z serwisu</h2>
<p>Przycisk "Przypisz z serwisu" umożliwia przepisanie własności META ustawionych dla wskazanego <a href="services_general.php">serwisu</a> do okienka właściwości META. Kliknięcie tego przycisku rozwija listę serwisów dostępnych w Pająku. Wskazanie serwisu na liście spowoduje przeniesienie jego własności (określonych w oknie <a href="service_properites.php#metaindexing">Właściwości serwisu</a>) do odpowiednich pól "Edytora sekcji META".

