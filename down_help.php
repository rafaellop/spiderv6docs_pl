<p>Okno wywołuje się za pomocą skrótu ALT+F9 lub pozycji z <a href="main_menu_main.php">Menu głownego</a> \ <a href="main_menu_view.php">Wyświetl</a>. Dodatkowo okno jest wywoływane automatycznie w przypadku użycia funkcji pomocy kontekstowej do znaczników lub elementów języka programowania po wciśnięciu skrótu CTRL+F1 na wybranym poleceniu języka HTML, CSS, PHP.</p>

<p>W oknie tym dostępne są tematy pomocy dla różnych języków obsługiwanych przez Pająka (HTML, JavaScript, PHP, itd), a także pomoc online.</p>

<p>Do obsługi tego okna pomocy możesz użyć przycisków menu z prawej strony:</p>

<p><b>Poprzednia strona</b>, <b>Następna strona</b><br />
Kliknij, aby przejść na poprzednią, lub następną stronę Pomocy w aktualnym temacie.</p>

<p><b>Otworzenie bieżącej strony w nowym oknie</b><br />
Kliknij aby otworzyć aktualną stronę Pomocy w nowym oknie domyślnej przeglądarki internetowej.</p>

<p><b>Kanały RSS</b><br />
Wyświetla menu umożliwiające przeglądanie kanałów najnowszych wiadomości RSS i zarządzanie nimi. Układ poleceń jest identyczny jak w <a href="main_menu_help.php#rss" title="Menu Pomoc / Kanały RSS">Menu Pomoc / Kanały RSS</a>. Więcej o kanałach RSS czytaj w temacie <a href="settings_network_rss.php" title="Ustawienia kanałów RSS">Ustawienia kanałów RSS</a></p>

