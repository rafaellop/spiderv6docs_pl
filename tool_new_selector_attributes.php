<p><b>Wartość</b><br /><br />
W tym polu podaj wymaganą wartość bieżącego atrybutu znacznika. Pole to może być aktywne, bądź nieaktywne w zależności od poniższych opcji:

<ul>
	<li><b>atrybut musi istnieć</b> - wybierz, jeśli wymagana jest tylko obecność atrybutu w znaczniku, bez względu na jego wartość (pole wartość będzie nieaktywne)</li>
	<li><b>atrybut musi istnieć i posiadać podaną wartość</b> (dla tej i dla następnych opcji pole wartość będzie aktywne)</li>
	<li><b>atrybut musi istnieć, a jego oddzielana spacjami lista wartości musi zawierać podaną wartość</b></li>
