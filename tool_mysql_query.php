<h1>Narzędzia - Wykonaj zapytanie SQL</h1>



<p>To okienko służy to wprowadzania własnego zapytania do bazy danych. </p>

<p>Aby wykonać zapytanie, wprowadź je do pola tekstowego zajmującego większa część okienka. 

<p>Obok pola znajdziesz przyciski:

<p><b>Lista Ulubionych zapytań</b><br />
Kliknij, aby wyświetlić listę <b>Ulubionych tabel i zapytań</b>. Możesz z niej wybrać dodane przez siebie uprzednio zapytanie.

<p><b>Dodaj do Ulubionych zapytań</b><br />
Kliknij aby dodać bieżące zapytanie do listy <b>Ulubionych zapytań</b>. Dzięki temu zostanie ono zapamiętane do późniejszego wykorzystania.

<p><b>Usuń z Ulubionych zapytań</b><br />
Kliknij, aby wyświetlić listę <b>Ulubionych zapytań</b> w celu wybrania z niej pozycji do usunięcia.

<p><b>Zawijaj wiersze</b><br />
Kliknij, aby włączyć zawijanie wierszy w polu tekstowym.

<p>Rekordy pobrane wskutek wykonania zapytania mogą zostać wyświetlone na liście <a href="main_tabs_inspector_mysql.php#table_content">zawartość tabeli</a> w dolnej części zakładki <a href="main_tabs_inspector_mysql.php">Inspektor MySQL</a>.</p>

