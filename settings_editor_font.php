<h1>Ustawienia programu - Czcionka edytora</h1>

<!-- treść pomocy -->
<p>
Ustawienia zgrupowane w kategorii "Czcionka edytora" pozwalają na dostsowanie edytora dokumentów do własnych upodobań w zakresie używanej czcionki. Możesz tutaj wybrać jaka czcionka spośród dostępnych w systemie będzie czcionką do edycji dokumentów. W grupie tej dostępne są następujące ustawienia:

<p><b>Używaj czcionki o stałej szerokości (działa szybciej)</b><br>
Zaznacz tę opcję, aby używać w edytorze czcionki o stałej szerokości. Włączenie tej opcji powoduje szybsze działanie edytora, ale wybór czcionek jest węższy.

<p><b>Używaj wskazanej czcionki (działa nieco wolniej)</b><br>
Zaznacz tę opcję, aby używać w edytorze dowolnie wybranej czcionki. Włączenie tej opcji powoduje nieco wolniejsze działanie edytora, ale możesz wybrać w zasadzie dowolną czcionkę.

<p><b>Krój czcionki</b><br>
Z tej listy wybierz czcionkę, której chcesz używać.

<p><b>Zestaw znaków</b><br>
Z tej listy wybierz zestaw znaków dla wybranej czcionki.

<p><b>Rozmiar czcionki</b><br>
W tym polu wskaż rozmiar dla czcionki używanej do edycji dokumentów.

<p><b>Kolor tekstu zaznaczenia</b><br>
Z tej listy wybierz kolor, którego chcesz użyć jako kolor tekstu dla zaznaczonego tekstu.

<p><b>Kolor tła zaznaczenia</b><br>
Z tej listy wybierz kolor, którego chcesz użyć jako kolor tła zaznaczonego tekstu.

<p><b>Podgląd ustawień czcionki</b><br>
W tym miejscu możesz zobaczyć jak będzie się prezentować w edytorze wybrana czcionka.

