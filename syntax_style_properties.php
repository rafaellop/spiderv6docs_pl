<h1>Edytor schematów kolorowania - Właściwości stylu</h1>


<!-- treść pomocy -->

Okno służy do definiowania kolorów i stylu tekstu dla danego stylu. Zostało podzielone na trzy części: 'Nazwa stylu', 'Kolor tła' i 'Właściwości tekstu'.


<p><b>Nazwa stylu</b><br />
W tym polu wpisz nazwę dla stylu.</p>
<p>Nazwa powinna jednoznacznie określać typ kodu, dla jakiego ma mieć zastosowanie (np. 'HTML tag name', 'JavaScript function'), co pozwoli przy budowaniu schematu łatwo przyporządkować dany styl do słów kluczowych itp. w innych oknach programu.</p>
<b>Kolor tła</b>
<ul>
	<li><b>domyślny</b><br />	Wybierz tę opcję, jeśli kolor tła ma pozostać domyślny (zdefiniowany w systemie). Jest to opcja zalecana i ustawiona domyślnie.</li>
	<li><b>ten kolor</b><br />	Pozwala na wybór z listy dowolnego koloru tła. Pierwsze na liście umieszczono kilka predefiniowanych kolorów. Wybranie ostatniej pozycji 'Wybierz..." spowoduje otwarcie okna, w którym będzie można wybrać dowolny kolor.</li>
</ul>

<b>Właściwości tekstu</b>
<ul>
	<li><b>Kolor tekstu</b><br />	Pozwala na wybór z listy dowolnego koloru dla tekstu. Pierwsze na liście jest kilka predefiniowanych kolorów. Wybranie ostatniej pozycji 'Wybierz..." spowoduje otwarcie okna, w którym będzie można wybrać dowolny kolor.</li>
	<li><b>Styl tekstu</b><br />	Możesz zaznaczyć style, jakie mają zostać zastosowane do tekstu (pogrubienie, kursywa, podkreślenie, przekreślenie)</li>
</ul>





