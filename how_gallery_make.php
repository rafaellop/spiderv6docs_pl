<h1>Galeria grafik</h1>
<!-- nagłowek głowny tematu -->

<!-- Treść pomocy -->

<p>Na stronach często spotykane są galerie zawierające zdjęcia, obrazki i inne grafiki. Pająk umożliwia bardzo szybkie i łatwe utworzenie takiej galerii. Mogą to zrobić nawet osoby całkowicie nieznające się na tworzeniu stron.</p>

<h2>Jak utworzyć galerię obrazków?</h2>

<p>W celu utworzenia galerii uruchom <a href="tool_gallery.php">Kreatora galerii obrazków</a>. </p>

<p>Pierwsza plansza jest jest powitaniem i wprowadzeniem do narzędzia. Kreator galerii będzie Ciebie prowadzić przez proces jej tworzenia. W kolejnych planszach będziesz:

<ul>
   <li>wybierać obrazki do galerii (możesz dodać automatycznie wszystkie pliki z wybranego folderu, bądź z kilku folderów)</li>
   <li>ustawiać opcje wyglądu galerii (liczbę i układ wierszy i kolumn, obrazki i położenie przycisków, wystrój graficzny galerii)</li>
   <li>wskazywać położenie docelowych plików galerii (dokumntów html oraz obrazków i ich automatycznie wygenerowanych miniatur w wybranym formacie)</li>
   <li>ustawiać opcje tworzenia miniatur (sposób skalowania, wymiary, stopień kompresji)</li>
</ul>

<p>Na ostatniej planszy będziesz mógł obejrzeć gotowa galerię jeszcze przed zapisem jej w docelowym miejscu na dysku - jeśli będzie trzeba, wróć do poprzednich plansz i popraw odpowiednie opcje.</p>

<p>Szczegółowych informacji na temat kreatora szukaj w pomocy <a href="tool_gallery.php">Kreatora galerii obrazków</a> uruchamianej po kliknięciu w przycisk Pomoc w oknie narzędzia.</p>





