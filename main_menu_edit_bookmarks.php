<h1>Menu główne - Edycja/Zakładki w dokumencie</h1>

<!-- Treść pomocy -->
<p>Menu to pozwala poruszać się po dokumencie przy wykorzystaniu <a href="main_editor_bookmarks.php">zakładek</a> i innych elementów. Dostępne jest jako podmenu w menu głównym <a href="main_menu_edit.php">Edycja</a>, lub po kliknięciu na rynnę z prawej strony okna edytora.. Dostępne są następujące polecenia:</p>

<p><b>Idź do zakładki</b><br />
Kliknij, aby przejść do wybranej zakładki numerowanej.

<p><b>Ustaw zakładkę</b><br />
Kliknij, aby dodać zakładkę numerowaną w bieżącej linii.

<p><b>Usuń zakładkę</b><br />
Kliknij, aby usunąć wybraną, bądź też wszystkie zakładki numerowane.

<p><b>Ustaw pierwsza wolną zakładkę</b><br />
Kliknij, aby ustawić w bieżącej linii pierwszą wolną zakładkę numerowaną (domyślny skrót <tt>Ctrl+K</tt>).

<hr>

<p><b>Idź do zakładki nazwanej</b><br />
Kliknij, aby przejść do wybranej zakładki nazwanej.

<p><b>Ustaw zakładkę nazwaną</b><br />
Kliknij, aby dodać zakładkę nazwaną w bieżącej linii.

<p><b>Usuń zakładkę nazwaną</b><br />
Kliknij, aby usunąć zakładkę nazwaną.

<hr>

<p><b>Idź do etykiety dokumentu</b><br />
Kliknij, aby przejść do wybranej etykiety w dokumencie.

<p><b><a href="find_text.php#goto">Idź do linii nr</a></b><br />
Kliknij, aby wybrać linię, do której chcesz przejść(domyślny skrót <tt>Ctrl+G</tt>).

<hr>

<p><b><a href="tool_docinfo_todo_add.php">Dodaj zadanie do wykonania</a></b><br />
Kliknij, aby dodać zadanie do wykonania.

<p><b><a href="tool_docinfo.php#todotasks">Zadania do wykonania</a></b><br />
Kliknij, aby otworzyć okno informacji o biężącym dokumencie i wyświetlić w nim listę zadań do wykonania. 


