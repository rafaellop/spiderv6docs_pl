<h1>Ustawienia programu - Przy starcie</h1>

<!-- treść pomocy -->
<p>
Kategoria o nazwie "Przy starcie" pozwala na określenie zachowania programu podczas uruchamiania. Możesz dzięki temu wskazać folder, do którego ma się przełączać, włączyć otwieranie ostatnio edytowanych dokumentów, itp. W tej grupie znajdują się następujące ustawienia:

<p><b>Wyświetlaj okno "O programie" przy starcie</b><br>
Zaznacz tę opcję, aby włączyć wyświetlanie okna informacji o programie przy starcie programu.

<p><b>Wyświetlaj powitanie i wybór trybu pracy z programem</b><br>
Zaznacz tę opcję, aby włączyć wyświetlanie okna powitalnego programu wraz z możliwością wyboru trybu pracy głównego okna programu.

<p><b>Wyświetlaj okno "Podpowiedzi" przy starcie programu</b><br>
Zaznacz tę opcję, aby włączyć wyświetlanie okienka podpowiedzi i sztuczek przy starcie programu.

<p><b>Pokaż ikonę w zasobniku obok zegara i minimalizuj do niej</b><br>
Zaznacz tę opcję, aby włączyć minimalizowanie okna programu do ikony umieszczonej w zasobniku systemowym obok zegara, zamiast do paska zadań. Jej wyłączenie powoduje normalne minimalizowanie programu i wyłączenie ikony w zasobniku. Po kliknięciu ikony w zasobniku prawym klawiszem pojawia się menu z kilkoma przydatnymi poleceniami (przywrócenie okna programu, wyłączenie programu, odsyłacze do stron o Pająku w Internecie itp.).

<p><b>Ostatnio używany folder jako aktualny przy starcie programu</b><br>
Zaznacz tę opcję, aby spowodować ustawianie aktywnego folderu zawsze na ten sam folder, który był używany przy ostatnim uruchomieniu programu.

<p><b>Zawsze ten sam folder jako aktualny przy starcie programu</b><br>
Zaznacz tę opcję, aby spowodować ustawianie aktywnego folderu na wybrany niżej folder.

<p><b>Folder startowy</b><br>
W tym polu wprowadź lub wybierz za pomocą przycisku obok folder, który będzie ustawiany jako aktywny po uruchomieniu programu.

<p><b>Pamiętaj edytowane dokumenty dla programu</b><br>
Zaznacz tę opcję, aby program otwierał wszystkie dokumenty, które były otwarte przy poprzednim zamykaniu programu.

<p><b>Pamiętaj edytowane dokumenty dla osobno dla każdego serwisu</b><br>
Zaznacz tę opcję, aby program otwierał wszystkie dokumenty, które były otwarte podczas edycji każdego serwisu z osobna.

<p><b>Nie zapamiętuj edytowanych dokumentów</b><br>
Zaznacz tę opcję, aby Pająk nie zapamiętywał listy ostatnio otwartych dokumentów.



